#ifndef AUTO_DAMPER_POSITIONS_H
#define AUTO_DAMPER_POSITIONS_H

#include <stdbool.h>

//////////////////////////////////////////////////////////////
// Config
//////////////////////////////////////////////////////////////

#define POSITION_MAX_SLOTS 10
#define POSITION_LABEL_MAX 15

//////////////////////////////////////////////////////////////
// Position Data
//////////////////////////////////////////////////////////////

struct position {
  bool active;
  char label[POSITION_LABEL_MAX + 1];
  double angle;
};

//////////////////////////////////////////////////////////////
// Public API
//////////////////////////////////////////////////////////////

int positions_init(void);
const struct position *positions_get(int id);
int positions_set(int id, const char *label, double angle);
int positions_delete(int id);
int positions_count(void);

#endif
